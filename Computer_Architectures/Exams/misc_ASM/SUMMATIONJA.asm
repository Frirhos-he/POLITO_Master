.MODEL small
.STACK
.DATA

SOURCE DW 10,20,100,10000
       DW 0,7000,1,2
       DW 9000,12345,999,30000
       DW 200,210,7,65000
MAPP DB 16 DUP(?)
CROSS DW ?
.CODE
.STARTUP
         
XOR AX,AX
XOR BX,BX
XOR CX,CX
XOR SI,SI
MOV CROSS,0

loopi:

MOV DI,SI
SHL DI,1
MOV AX,WORD PTR SOURCE[DI]
CMP AX,0xFF
JA goon

MOV BYTE PTR MAPP[SI],1
ADD CROSS,AX
goon:

INC SI
CMP SI,16
JL loopi         

.EXIT